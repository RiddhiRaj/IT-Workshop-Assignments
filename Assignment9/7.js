// Develop a JavaScript program to display a message “HI! GOOD MORNING”. When a page is loaded and display a message “THANKS FOR VISITING OUR WEB PAGE” when a page is un-loaded.


window.onload = function() {
    alert("HI! GOOD MORNING");
}

window.onunload = function() {
    alert("THANKS FOR VISITING OUR WEB PAGE");
}

